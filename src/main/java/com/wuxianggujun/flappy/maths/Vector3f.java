package com.wuxianggujun.flappy.maths;

public class Vector3f {
    public float x;

    public float y;
    public float z;

    public Vector3f(){
        this.x = 0.0f;
        this.y = 0.0f;
        this.z = 0.0f;
    }

    public Vector3f(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }


}
